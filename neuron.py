# import the library tkinter
from tkinter import *

# Start of GUI

root_neuron = Tk()

# Geometry
root_neuron.geometry("900x600+150+120")  # determines the size of the root 900x600 the size 150 + 120 the position
root_neuron.maxsize(900, 600)
root_neuron.minsize(900, 600)
root_neuron.title("Neuron EndPoint")


root_neuron.mainloop()  # it shows the root when we run the program

# End of GUI
